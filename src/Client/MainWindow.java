    private void updateInterface(){
        
        sortTables();
        
        if(tables.size()>0){
        for(int i=0;i<tables.size();i++){
                switch(i){
                        case 0:
                            btnmasa1.setEnabled(tables.get(i).isAvailable());
                            break;
                        case 1:
                            btnmasa2.setEnabled(tables.get(i).isAvailable());
                            break;
                        case 2:
                            btnmasa3.setEnabled(tables.get(i).isAvailable());
                            break;
                        case 3:
                            btnmasa4.setEnabled(tables.get(i).isAvailable());
                            break;
                }
            
            switch (i) {
                case 0:
                    masa1.setText(Integer.toString(tables.get(i).getNumOfPlayers()));
                    break;
                case 1:
                    masa2.setText(Integer.toString(tables.get(i).getNumOfPlayers()));
                    break;
                case 2:
                    masa3.setText(Integer.toString(tables.get(i).getNumOfPlayers()));
                    break;
                case 3:
                    masa4.setText(Integer.toString(tables.get(i).getNumOfPlayers()));
                    break;
            }
        }
        
        }
        else{
              btnmasa1.setEnabled(false);
              btnmasa2.setEnabled(false);
              btnmasa3.setEnabled(false);
              btnmasa4.setEnabled(false);
              masa1.setText("");
              masa2.setText("");
              masa3.setText("");
              masa4.setText("");
        }
        if(user.getName() != null){
            jLabel3.setText(user.getName());
        }
        if(user.getCredit() != 0){
            jLabel4.setText(Integer.toString(user.getCredit())); 
        }
    }
    
