    private void update() throws IOException{
               new Thread() {
                @Override
                public void run() {
                    try{
                        while(!Thread.interrupted()){
                            try {
                                Object received = inFromServer.readObject();
                                if(received instanceof ArrayList){
                                    ArrayList<Table> tablesReceived = (ArrayList<Table>) received;
                                    tables.clear();
                                    tables=tablesReceived;
                                }
                                if(received instanceof MainServerUser){
                                    MainServerUser receivedUser = (MainServerUser) received;
                                    user = new MainServerUser();
                                    user = receivedUser;
                                }
                                updateInterface();

                            } catch (ClassNotFoundException ex) {
                                Logger.getLogger(MainWindow.class.getName()).log(Level.SEVERE, null, ex);
                            }
                        }
                    }
                    catch (IOException ex) {
                        Logger.getLogger(MainWindow.class.getName()).log(Level.SEVERE, null, ex);
                    }
                }
            }.start();
    }
   
            
       
               
    
    
